def estimate_impact_scenarios(
    passive_aum_billion: float = 120.0,
    weight_changes_bps: tuple[float, ...] = (5.0, 10.0, 20.0),
    stock_price: float = 50.0,
    adv_billion: float = 2.0,
    annualized_volatility: float = 0.35,
    impact_multiplier: float = 1.0,
) -> pd.DataFrame:
    rows: list[dict[str, float | str]] = []
    daily_vol = annualized_volatility / math.sqrt(252)
    adv = adv_billion * 1_000_000_000
    for weight_bps in weight_changes_bps:
        weight_change = weight_bps / 10_000
        required_notional = passive_aum_billion * 1_000_000_000 * weight_change
        required_shares = required_notional / stock_price
        participation = required_notional / adv if adv else 0.0
        sqrt_impact = daily_vol * math.sqrt(max(participation, 0.0)) * impact_multiplier
        rows.append(
            {
                "权重变化(bps)": weight_bps,
                "被动资金AUM(十亿美元)": passive_aum_billion,
                "估计必须成交金额(美元)": required_notional,
                "估计必须成交股数": required_shares,
                "相对ADV占比": participation,
                "平方根冲击估计(%)": sqrt_impact * 100,
            }
        )
    return pd.DataFrame(rows)


